export const Add = (a:number,b:number) => {
  return a+b
}

export const Subtract = (a:number,b:number) => {
  return a-b
}

export const Multiply = (a:number,b:number) => {
  return a*b
}

export const Division = (a:number,b:number) => {
  return a/b
}

export const Modulo = (a:number,b:number) => {
  return a%b
}

